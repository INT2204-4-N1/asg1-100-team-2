    /**
     * Hàm dịch từ được truyền vào 
     * @param word từ cần dịch nghĩa
     */
    public void TranslateWord(String word){
        if(search_online && E_V){
            Label_explain.setText(EV.TranslatebyAPI(word));
        }else if(!search_online && E_V){
            Label_explain.setText(EV.translateWordExplain(word));
        }else if(search_online && !E_V){
            Label_explain.setText(VE.TranslatebyAPI(word));
        }else{
            Label_explain.setText(VE.translateWordExplain(word));
        }
            
    }
    
    public void addSearchHistory(String word){
        SearchHistory.remove(word);
        SearchHistory.add(0,word);
        History_List.setListData(SearchHistory);
    }
    
