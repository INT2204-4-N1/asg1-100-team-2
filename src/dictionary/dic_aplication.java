    public void list_word(){
        SearchHistory.add(" ");
        History_List.setListData(SearchHistory);
        
        DefaultListModel model1 = new DefaultListModel();
        if(E_V){
        for (int i = 0; i < EV.keys.size(); i++) {
                model1.addElement(EV.keys.get(i));
            }
        } else {
            for (int i = 0; i < VE.keys.size(); i++) {
                model1.addElement(VE.keys.get(i));
            }
        }
        List_Words.setModel(model1);
    }
    private void Text_SearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Text_SearchKeyReleased
        // TODO add your handling code here:
        ArrayList l;
            DefaultListModel model1 = new DefaultListModel();
            String word = Text_Search.getText();
            if (word.isEmpty()) {
                list_word();
            } else {
                if(E_V){
                l = EV.searchKey(word);
                } else{
                    l = VE.searchKey(word);
                }     
                for (int i = 0; i < l.size(); i++) {
                model1.addElement(l.get(i));
                }
                List_Words.setModel(model1);
            }
            
    }//GEN-LAST:event_Text_SearchKeyReleased

