    /**
     * hàm thêm từ vào list_Word
     */
    private void List_Word(){
     String[] words = new String[dic.getNum()];
     Iterator<String> iterator = dic.Words.keySet().iterator();
     int i=0;
     while (iterator.hasNext()) {
        words[i]=iterator.next();
        i++;
     }
     List_Words.setListData(words);
    }

