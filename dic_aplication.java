    private void Text_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Text_SearchActionPerformed
        // TODO add your handling code here:
        String word_target = Text_Search.getText();
        if(search_online){
            String str;
            try {
                str = GoogleTranslate.translate("en","vi",word_target);
                Label_explain.setText(str);
            } catch (IOException ex) {
                Logger.getLogger(dic_aplication.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            Label_explain.setText(dic.translateWordExplain(word_target));
        }
        
    }//GEN-LAST:event_Text_SearchActionPerformed

