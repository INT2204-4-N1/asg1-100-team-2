    /**
     * xử lí sự kiện tạo âm thanh khi ấn Button_Voice
     * @param evt biến nhận sự kiện
     */
    private void Button_VoiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_VoiceActionPerformed
        // TODO add your handling code here:
       String Word = Text_Search.getText();
       if(search_online){
           try {
               Audio audio = Audio.getInstance();
               InputStream sound = null;
               try {
                   sound = audio.getAudio(Word, Language.ENGLISH);
               } catch (IOException ex) {
                   Logger.getLogger(dic_aplication.class.getName()).log(Level.SEVERE, null, ex);
               }
               audio.play(sound);
           } catch (JavaLayerException ex) {
               Logger.getLogger(dic_aplication.class.getName()).log(Level.SEVERE, null, ex);
           }
       }else{   
            dic.makeVoice(Word);
       }
    }//GEN-LAST:event_Button_VoiceActionPerformed

